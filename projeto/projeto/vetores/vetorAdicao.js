// adicao de um nome utilizando o push que coloca por ultimo

let nomes1 = ["Emolly", "Micaelly", "Daiane"];

nomes1.push ("Estevao");

console.log(nomes1);

// adicao de nome colocando em primeiro utilizando o unshift

let nomes2 = ["Micaely", "Emolly"];

nomes2.unshift("Estevao");

console.log(nomes2);